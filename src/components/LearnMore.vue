      <p class="title">Abstract:</p>
      <p class="subheading">There are a plethora of activities to do around us. From having a picnic in front of the Eiffel Tower to going skydiving over the Hawaiian Islands, there are countless activities and places to experience throughout the world. Sometimes the number of events to accomplish can be overwhelming, and one might not know what to do first, especially if they are traveling in a foreign country. While it’s a good idea to plan which activities to choose for your itinerary, there will be times when you will end up wondering what to do next merely because one is not inspired enough. Instead of taking action, we are stuck looking through other people’s Instagram or Facebook page wishing that we are doing the fun activities that they are doing. <br><br>Even with so many activities available to do, it is common to find one’s self-sitting at home not doing anything on a beautiful Saturday morning because they cannot decide what they want to do. This may result from a lack of planning or lack of inspiration, but this ultimately leads to people missing out on experiencing what there is to do around them. Another problem that arises with traveling, in general, is that people do not stay in a single location for very long. This means that travelers only have a short amount of time to wander and explore places, and is most likely given just one opportunity to experience the area. Also, another common problem is that wrong planning leads to missing out on the most fun activity, going to the best events, or visiting the best places in the area. In turn, this leads to an unpleasant experience and biased commentaries.
      </p>
      <p class="title">What is BuckIt?:</p>
      <p class="subheading">BuckIt! is an application that allows users to create their own personal bucket list. BuckIt! also serves as a location-based activity suggestion platform which enables locals to post suggestions for activities, and example itineraries for other people to see. A voting system allows the suggestions to be upvoted and commented by others. This upvoting feature allows other users to evaluate which events are the most popular in the area. Suggestions also allow for comments by other users to give additional information so that people can be well informed and prepared. BuckIt’s applications interface gives users access to activities that are suggested by locals which means that they will gain local knowledge of which activities are fun, or which item on the restaurant menu is the most popular. People who use BuckIt! will have the most authentic experience possible at any given time. BuckIt!’s trending page recommends users which activities are most popular based on their location and preferences so that they are given certain ideas of what to do. Users who comment on suggestions may help other users by providing additional information for them to consider upon reading those comments. For example, one comment could be “make sure to bring an umbrella as the weather can change suddenly.” It is these features of BuckIt! that makes it a unique application; one that enhances the user’s travel experience.
      </p>
